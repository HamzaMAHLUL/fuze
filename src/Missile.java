public class Missile {

  final private double Menzil ;
  final private double Agirlik ;


public Missile(double menzil , double agirlik )
{
    this.Menzil = menzil;
    this.Agirlik = agirlik;
}

public double getMenzil(){

    return  Menzil ;
}

public  double getAgirlik(){

    return  Agirlik;
}


}
